<h2>About Thakur Polytechnic  </h2>

<p>Thakur Polytechnic is aﬃliated to the Maharashtra State Board of Technical  Education (MSBTE) and conduct courses approved by the All India Council for Technical Education (AICTE), New Delhi and recognized by Govt. of Maharashtra. It fulﬁls all the norms and standards set by the AICTE as well as the Maharashtra State Board of Technical Education.  The Polytechnic was accorded approval by AICTE  vide their letter no. F-740-80-819 - (F)/FT/98 dt. 24th July 1998.</p>
<p>Thakur Polytechnic  (Tpoly) was established in the year 1998. It is primarily  to cater to the need of students desirous of pursuing technical education after the Secondary School Certiﬁcate examination. It provides the best infrastructure facilities  matched by an equally competent teaching faculty and a right academic ambience to make learning a delight full experience.</p>
<p>As reﬂected in mission statement the Tpoly has set upon itself a responsibility of providing quality technical education comparable to international standards and preparing students with vocational excellence. To meet the quality assurance requirement, the institute has opted for ISO 9001:2015 from BSI (British Standard Institution). To ensure quality education, management is committed to render quality technical education is striving continually to improve its eﬀectiveness by taking relevant measures.</p>
<p>Thakur Polytechnic was accrediated by National Board of Accreditation (NBA), New Delhi with eﬀect from 15-03-2012 for three years for Mechanical Engineering, Electronics Telecommunication Engineering Electronics Engineering. The Institute re- accredited for 2nd time accreditation for Five courses i.e. Mechanical  Engineering, Electronics  Telecommunication Engineering, Electronics Engineering, Computer Engineering Information Technology for Two years with eﬀect from 01/06/2017.</p>
<p>Thakur Polytechnic  has been conferred with  the ISTE (Indian  Society for Technical Education) Narsee Monjee Best Overall Performance Polytechnic award for Western Region in a ceremony held at the 47th ISTE National Annual convention Saintgits College of Engineering, Kottayam, Kerala on 27th January 2018.</p>
<p>Thakur Polytechnic has been awarded Best Laboratory Award to Metrology Quality Control (MQC) Lab of Mechanical Engineering Department by Maharashtra State Board of Technical Education (MSBTE) cash Prize of Rs. 50,000/- for further development of Lab.</p>
<p>Thakur Polytechnic received permission to conduct specialized courses from AICTE for Skill Development under Pradhan Mantri Kaushalya Vikas Yojana (PMKVY) for various courses.</p>



